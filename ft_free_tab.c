#include "libft.h"

void	ft_free_tab(char **tab)
{
	int	i;

	i = 0;
	if (tab)
	{
		while (tab[i])
		{
			ft_memdel(tab[i]);
			i++;
		}
		ft_memdel(tab);
	}
}
